"""
Задание
Напишите программу, которая сможет вычислять площади трех фигур (круг, треугольник и прямоугольник). 
Тип фигуры запрашиваем через пользовательский ввод, после чего делаем запрос характеристик фигуры:
если пользователь выбрал круг, запрашиваем его радиус,
если треугольник – длины трех его сторон;
если прямоугольник – длины двух его сторон.
"""

figure_type = input("Введите название фигуры (круг, треугольник, прямоугольник) ").lower()
if figure_type == "круг":
    radius = float(input("Введите радиус круга "))
    print("Площадь круга - ", math.pi * radius * radius)
elif figure_type == "треугольник":
    print("Введите стороны треугольника")
    a = float(input())
    b = float(input())
    c = float(input())
    p = (a + b + c) / 2
    print("Площадь треугольнкиа - ", math.sqrt(p * (p - a) * (p - b) * (p - c)))

elif figure_type == "прямоугольник":
    print("Введите стороны прямоугольника")
    a = float(input())
    b = float(input())
    print("Площадь прямоугольника - ", a * b)
